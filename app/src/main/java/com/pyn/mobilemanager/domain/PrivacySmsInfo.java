package com.pyn.mobilemanager.domain;

/**
 * 隐私保护名单的电话号码和名字实体类
 */
public class PrivacySmsInfo {

	private String number;
	private String name;

	public String getNumber() {
		return number;
	}

	public void setNumber(String number) {
		this.number = number;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
